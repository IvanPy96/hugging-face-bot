def is_gigachat(model_id: str) -> bool:
    """Return True when the model ID belongs to a GigaChat variant."""
    return "gigachat" in model_id.lower()


def gigachat_roast() -> str:
    """Return a random roast message for GigaChat models."""
    return random.choice(GIGACHAT_ROASTS)


def count_tier(n: int) -> str:
    """Determine the size tier based on model count."""
    if n == 0:
        return "zero"
    if n <= 9:
        return "small"
    if n <= 29:
        return "growing"
    if n <= 99:
        return "active"
    if n <= 499:
        return "big"
    return "mega"


def stat_comment(count: int, org: str = "") -> str:
    """Generate a humorous comment based on the organisation's model count."""
    tier = count_tier(count)
    if org in ORG_STAT_COMMENTS:
        options = ORG_STAT_COMMENTS[org].get(tier, [])
        if options:
            return random.choice(options)
    return random.choice(GENERIC_STAT_COMMENTS.get(tier, ["–ò–Ω—Ç–µ—Ä–µ—Å–Ω–æ... ü§î"]))
